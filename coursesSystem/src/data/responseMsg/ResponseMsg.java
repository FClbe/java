package data.responseMsg;

public class ResponseMsg {
    private int fig;
    private String msg;
    private String data;

    public int getFig() {
        return fig;
    }

    public void setFig(int fig) {
        this.fig = fig;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
